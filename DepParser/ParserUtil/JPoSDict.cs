        public JPoSDict()
        {
            MajorPoSDict = new Dictionary<int, string>();
            MinorPoSDict = new Dictionary<int, string>();
            InflectionTypeDict = new Dictionary<int, string>();
            InflectionFormDict = new Dictionary<int, string>();

            for (int i = 0; i < PoSNames.Length; ++i)
            {
                string[] parts = PoSNames[i].Split(new string[] { "," }, StringSplitOptions.RemoveEmptyEntries);
                MajorPoSDict[i] = parts[0];
                MinorPoSDict[i] = string.Format("{0},{1},{2},{3}", parts[0], parts[1], parts[2], parts[3]);
                if (parts[4] != "*")
                {
                    InflectionTypeDict[i] = parts[4];
                }
                if (parts[5] != "*")
                {
                    InflectionFormDict[i] = parts[5];
                }
            }
        }

        public string MajorPoS(int id)
        {
            return MajorPoSDict[id];
        }

        public string MinorPoS(int id)
        {
            return MinorPoSDict[id];
        }

        public string InflectionType(int id)
        {
            string type;
            if (InflectionTypeDict.TryGetValue(id, out type))
            {
                return type;
            }
            else
            {
                return null;
            }
        }

        public string InflectionForm(int id)
        {
            string form;
            if (InflectionFormDict.TryGetValue(id, out form))
            {
                return form;
            }
            else
            {
                return null;
            }
        }

